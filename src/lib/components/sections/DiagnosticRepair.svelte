        Diagnostic & Repair Visits

        At Amalfii Heating we aim to be clear and upfront about how our repair visits are booked and charged, so you know exactly what to expect.
        This approach allows us to work safely and sustainably while continuing to provide a reliable, professional service.

        Diagnostic Visits
        All boiler, heating and heat pump repairs are booked as a 1-hour diagnostic visit.
        This covers:
        •	engineer attendance
        •	fault finding
        •	essential safety checks
        •	up to the first hour of labour
        Where possible, repairs will be completed within this time. If the issue can be resolved during the diagnostic visit, no additional labour is charged.


        If Parts or Further Work Are Required
        Some faults require parts to be ordered or additional labour beyond the first hour.
        If this is the case, this will always be explained clearly before any further work is carried out.
        Any return visit or additional labour will be quoted in advance and charged in 30-minute increments.

        Repeat Visits & Ongoing Issues
        Heating systems can occasionally have more than one underlying issue. Once circulation improves or a fault is resolved, other unrelated faults may sometimes become apparent.
        We always stand by our workmanship. If the same repair fails due to our work, we will of course return to put this right. If a separate or underlying fault is identified, this will be discussed with you and a further diagnostic or repair visit may be required.

        Transparency & Fairness
        Our diagnostic approach allows us to:
        •	diagnose faults thoroughly
        •	work safely and professionally
        •	be clear and fair with all customers
        •	maintain a reliable, well-managed service
        All charges are explained at the time of booking and we are always happy to answer any questions before arranging a visit.

        Why We Use Diagnostic Visits
        As Amalfii Heating has grown, the volume and complexity of repair work has increased.
        Structured diagnostic visits allow us to assess faults properly, manage repairs consistently and continue providing a reliable service in a sustainable way.

        Price 
